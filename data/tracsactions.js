function searchTable(value, data){
    var fillteredData = []

    for(var i = 0; i < data.length; i++){
        value = value.toLowerCase()
        var name = data[i].Employee.toLowerCase()


        if(name.includes(value)){
            fillteredData.push(data[i])
        }
    }
    return fillteredData
}


